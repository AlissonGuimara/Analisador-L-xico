    public void borrarArchivo(String path){
        File archivo = new File(path);     
        archivo.delete();              
    }
    
    public void agregarTextoTarea(JTextArea area, String path){
        File archivo = new File(path); 
        try{
            BufferedReader leer=new BufferedReader(new FileReader(archivo));
            String linea=leer.readLine();
            while(linea!=null){
                area.append(linea+"\n");
                linea=leer.readLine();
            }
            leer.close();
            archivo.delete();
        }catch(Exception e){}
            
    }
    
    
    
    
    private String abrirArchivo(String path){
        String texto="",linea;
        FileReader fr = null;
        BufferedReader br = null;
        try {
            if(!path.substring(path.length()-4).equals(".txt")){
                JOptionPane.showMessageDialog(null, "No se puede abrir un archivo con esa extensi√≥n.","Error",JOptionPane.ERROR_MESSAGE);
                return "";
            }
            fr = new FileReader(new File(path));
            br = new BufferedReader(fr);
            while((linea=br.readLine())!=null){
                linea.replaceAll("\n","");
                texto+=linea+"\n";   
            }
        } catch (FileNotFoundException ex) {
            //Logger.getLogger(EntornoGrafico.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "No existe ese archivo\nCompruebe el nombre del archivo e intentelo de nuevo","Error",JOptionPane.ERROR_MESSAGE);
        } catch (IOException ex) {
            Logger.getLogger(EntornoGrafico.class.getName()).log(Level.SEVERE, null, ex);
        }
        finally{
            try{                    
                if( null != fr )fr.close();                  
            }catch (IOException e2){}
        }
       
        return texto;
    }
    private String guardar = "";
    private void guardar (String ruta){
        path=ruta;
        BufferedWriter bw;
        try {
            bw = new BufferedWriter(new FileWriter(path));
            bw.write(jTextPane1.getText().replaceAll("\n", "\r\n"));
            guardar=jTextPane1.getText();
            bw.close();
        } catch (IOException ex) {
            //Logger.getLogger(EntornoGrafico.class.getName()).log(Level.SEVERE, null, ex);
            guardarArchivoComo();
        }
    }
    
    private void guardarArchivoComo(){
        javax.swing.JFileChooser jF1= new javax.swing.JFileChooser();
        jF1.setFileFilter(new FileNameExtensionFilter(".txt", "txt"));
        
        if(path.length()!=0)
            jF1.setCurrentDirectory(new File(path));
        
        String ruta = ""; 
        try{ 
            if(jF1.showSaveDialog(null)==jF1.APPROVE_OPTION){ 
                ruta = jF1.getSelectedFile().getAbsolutePath(); 
                ruta = jF1.getSelectedFile().toString();
                if (!ruta .endsWith(".txt"))
                    ruta += ".txt";
                if(new File(ruta).exists()) 
                    if(JOptionPane.OK_OPTION == JOptionPane.showConfirmDialog(this,"Ya existe un archivo con este nombre,\n¬øDesea remplazarlo?","Titulo",JOptionPane.YES_NO_OPTION)) 
                        guardar(ruta);
                    
                File file = new File(ruta);
                file.createNewFile();
                guardar(ruta);
            } 
        }catch (Exception ex){ 
            showMessageDialog(null,"Ha ocurrido un error al momento de intentar guardar el archivo");
        } 
        
    }
        
